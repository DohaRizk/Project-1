<div id = "what">
	<h2 id = "what">What are online Books?</h2>
	<p>An on-line book is a resource in book-like form that is only available to read online. It differs from the common idea of an ebook, which is usually available to download and read locally or on an ebook reader.</p>
	<b>Book-like means:</b><br/>
		<ol>
		 	<li>information is presented in pages</li>
		 	<li>pages are normally read sequentially (though flipping to another page is possible)</li>
		 	<li>pages are read passively, with little or no interaction or multimedia</li>
		</ol>
	<p>Online means the content may only be read while the reader is online.</p>
	<p>Thus the reader's experience is similar to reading a printed book, except that the book is read at a computer and is only accessible while the reader is online.Online books are a common resource in virtual learning environments. For example, the Moodle VLE defines an online book in this way.</p>
	<p>You can read more about online books from <a id = "what" href="https://en.wikipedia.org/wiki/Online_book">here</a></p>
